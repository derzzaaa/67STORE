// Получить текущий язык из сессии или использовать английский по умолчанию
function getCurrentLanguage() {
    if (!isset($_SESSION['language'])) {
        $_SESSION['language'] = 'en'; // По умолчанию английский
    }
    return $_SESSION['language'];
}

// Установить язык
function setLanguage($lang) {
    if (in_array($lang, ['en', 'ru'])) {
        $_SESSION['language'] = $lang;
    }
}

// Получить перевод
function t($key) {
    global $translations;
    $lang = getCurrentLanguage();
    
    if (isset($translations[$lang][$key])) {
        return $translations[$lang][$key];
    }
    
    // Если перевод не найден, вернуть ключ
    return $key;
}

// Обработка переключения языка через GET параметр
if (isset($_GET['lang'])) {
    setLanguage($_GET['lang']);
    // Перенаправляем на ту же страницу без параметра lang
    $redirect = strtok($_SERVER['REQUEST_URI'], '?');
    header('Location: ' . $redirect);
    exit;
}
