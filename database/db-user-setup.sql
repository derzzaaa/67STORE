-- ============================================================
-- db-user-setup.sql — Настройка прав пользователя БД
-- ============================================================
-- Выполните этот скрипт от имени root-пользователя MySQL/MariaDB
-- Заменит root-доступ на пользователя с минимальными правами

-- ── 1. Создаём отдельного пользователя для приложения──────────────────────
-- Замените 'STRONG_PASSWORD_HERE' на надёжный пароль
CREATE USER IF NOT EXISTS 'seven_app'@'localhost' IDENTIFIED BY 'STRONG_PASSWORD_HERE';

-- ── 2. Выдаём только необходимые права (принцип минимальных привилегий) ───
GRANT SELECT, INSERT, UPDATE, DELETE ON seven_eleven_shop.* TO 'seven_app'@'localhost';

-- НЕ выдаём:
--   DROP, CREATE, ALTER  — изменение структуры
--   GRANT OPTION         — передача прав
--   FILE                 — доступ к файловой системе
--   EXECUTE              — выполнение хранимых процедур (если не нужны)

-- ── 3. Применяем изменения ────────────────────────────────────────────────
FLUSH PRIVILEGES;

-- ── 4. Проверяем права ────────────────────────────────────────────────────
SHOW GRANTS FOR 'seven_app'@'localhost';

